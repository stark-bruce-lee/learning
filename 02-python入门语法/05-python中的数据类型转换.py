# 字符型数据转换成整形数据
num1 = int("11")
print(type(num1), num1)

# 字符型数据转换成浮点型数据
num2 = float("13.14")
print(type(num2), num2)

# 整形，浮点型，转换成字符串
num3 = str(11)
print(type(num3), num3)
num4 = str(13.14)
print(type(num4), num4)

# 整型和浮点型的相互转换
num5 = float(11.25)
print(type(num5), num5)
num6 = int(13.84)
print(type(num6), num6)
