# 外层循环：表白100天的控制
# 内层循环：每天送的玫瑰花的控制
i = 1
while i < 100:
    print(f"今天是表白的第{i}天")
    j = 1
    while j <= 10:
        print(f"送给娜娜第{j}只玫瑰")
        j += 1
    print("娜娜，我喜欢你")
    i += 1
print(f"今天是表白的第{i}天，成功")





